"""Crie um programa que leia dois numeros inteiros e mostre a soma entre eles"""

n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))

s = n1 + n2

print("A soma de ", n1, "+", n2, "é igual a: ", s)

print("A soma ente {} e {} é igual a: {}".format(n1, n2, s))

print("A soma de n1 + n2 vale: {}".format(s))
